//Q5. Use the while loop to print numbers from 1 to 25 in ascending and descending order.

//Ascending order
let ascending_order = 1;
console.log("Printing in Ascending Order:")
while (ascending_order <= 25) {
    
    console.log(ascending_order);
    ascending_order++;
}
console.log("******************************************************************");
//Descending Order
let descending_order = 25;
console.log("Printing in Descending Order:")
while (descending_order >= 1) {
    
    console.log(descending_order);
    descending_order--;
}

